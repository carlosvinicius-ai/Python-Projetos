# Operadores aritméticos
# utilizados para fazer calculos desde os mais simples
# até os mais complexos

adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 3  # o resultado sempre é -> float
print('Divisão', divisao)

divisao_inteira = 10 // 3 # o resultado sempre é -> int
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2  # resto da divisão
print('Módulo', modulo)