# Aspas simples
print('Carlos Vinicius')

#Aspas duplas
print("Carlos Vinicius") # Devemos ter atenção em quando usar cada um deles

#Escape
print("Carlos \"Vinicius\"") # \ -> para ele ignorar no geral a próxima informação inserida

#r
print(r"Carlos \"Vinicius\"") # r -> utilizamos para expressões regulares