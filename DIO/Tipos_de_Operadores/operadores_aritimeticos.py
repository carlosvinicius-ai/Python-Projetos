produto_1 = 10
produto_2 = 20

# Adição 
print(produto_1 + produto_2 + 3.5)

# Subtração

print(produto_1 - produto_2)

# Divisão

print(produto_1 / produto_2)

#Divisão por inteiro

print(produto_1 // produto_2)

#Multiplicação

print(produto_1 * produto_2)

# Modulo

print(produto_1 % produto_2)

# Exponenciação

print(produto_1 ** produto_2)